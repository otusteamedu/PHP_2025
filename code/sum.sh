   #!/bin/bash

   # Проверка на количество аргументов
   if [ "$#" -ne 2 ]; then
       echo "Ошибка: требуется два аргумента."
       exit 1
   fi

   # Проверка на корректность чисел с помощью регулярного выражения
   if ! [[ $1 =~ ^-?[0-9]+(\.[0-9]+)?$ ]] || ! [[ $2 =~ ^-?[0-9]+(\.[0-9]+)?$ ]]; then
       echo "Ошибка: оба аргумента должны быть числами."
       exit 1
   fi

   # Суммирование и вывод результата
   sum=$(awk "BEGIN { print $1 + $2 }")
   echo "Сумма: $sum"
   
