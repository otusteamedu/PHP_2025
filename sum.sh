#!/bin/bash

if [ "$#" -ne 2 ]; then
    echo "Ошибка: требуется два параметра."
    exit 1
fi

if [ -z "$1" ] || [ -z "$2" ]; then
    echo "Ошибка: оба параметра не должны быть пустыми."
    exit 1
fi

if ! [[ "$1" =~ ^-?[0-9]+(\.[0-9]+)?$ ]] || ! [[ "$2" =~ ^-?[0-9]+(\.[0-9]+)?$ ]]; then
    echo "Ошибка: оба параметра должны быть числами."
    exit 1
fi

sum=$(awk -v a="$1" -v b="$2" 'BEGIN {print a + b}')

echo "Сумма чисел $1 и $2 равна: $sum"
