#!/bin/bash

# Проверка количества аргументов
if [ "$#" -ne 2 ]; then
    echo "Ошибка: Необходимо передать два числа."
    exit 1
fi

# Регулярное выражение для проверки, что аргументы являются числами (включая отрицательные и дробные)
re='^-?[0-9]+([.][0-9]+)?$'

if ! [[ $1 =~ $re ]]; then
    echo "Ошибка: Первый аргумент '$1' не является числом."
    exit 1
fi

if ! [[ $2 =~ $re ]]; then
    echo "Ошибка: Второй аргумент '$2' не является числом."
    exit 1
fi

# Вычисление суммы
sum=$(echo "$1 + $2" | bc)

# Вывод результата
echo "Сумма: $sum"