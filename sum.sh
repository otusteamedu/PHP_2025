#!/bin/bash

if [[ $# -ne 2 ]]; then
  echo "Ошибка: Введите 2 числовых аргумента."
  exit 1
fi

if ! [[ "$1" =~ ^[-+]?[0-9]+(\.[0-9]+)?$ && "$2" =~ ^[-+]?[0-9]+(\.[0-9]+)?$ ]]; then
  echo "Ошибка: Аргументы должны быть числами (целыми или вещественными)."
  exit 1
fi

sum=$(awk "BEGIN { print $1 + $2 }")
echo "Сумма: $sum"