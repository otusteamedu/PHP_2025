#!/bin/bash

# Проверка количества аргументов
if [ $# -ne 2 ]; then
    echo "Ошибка: Необходимо передать ровно два числа." >&2
    exit 1
fi

# Регулярное выражение для чисел (включая отрицательные и десятичные)
regex='^-?[0-9]+([.][0-9]+)?$'

if ! [[ $1 =~ $regex ]] || ! [[ $2 =~ $regex ]]; then
    echo "Ошибка: Оба аргумента должны быть числами." >&2
    exit 1
fi

# Вычисление суммы с помощью awk (более универсальный способ)
sum=$(awk "BEGIN {printf \"%.2f\", $1 + $2}")

echo "Сумма: $sum"