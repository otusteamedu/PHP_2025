// Тестовые случаи
$testCases = [
    [1, 2],       // Ожидаемый результат: "0.5"
    [2, 1],       // Ожидаемый результат: "2"
    [4, 333],     // Ожидаемый результат: "0.(012)"
    [-1, 3],      // Ожидаемый результат: "-0.(3)"
    [0, 5],       // Ожидаемый результат: "0"
