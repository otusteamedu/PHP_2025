#!/bin/bash

# Функция для проверки, является ли строка числом (целым или с плавающей запятой)
is_number() {
  # Используем регулярное выражение для проверки на число
  if [[ $1 =~ ^-?[0-9]+(\.[0-9]+)?$ ]]; then
    return 0 # Истина
  else
    return 1 # Ложь
  fi
}

# Проверяем количество аргументов
if [ "$#" -ne 2 ]; then
  echo "Ошибка: Необходимо передать два аргумента."
  exit 1
fi

# Проверяем, являются ли оба аргумента числами
if ! is_number "$1" || ! is_number "$2"; then
  echo "Ошибка: Оба аргумента должны быть числами."
  exit 1
fi

# Вычисляем и выводим сумму
((sum = $1 + $2))
echo "Сумма: $sum"
